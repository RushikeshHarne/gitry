"""
This module contains a simple function to greet a user.
"""

def greet(name: str) -> str:
    """
    Returns a greeting message.

    Args:
        name (str): The name of the person to greet.

    Returns:
        str: A greeting message.
    """
    return f"Hello, {name}!"

